    // Initialize Swiper for testimonials
    document.addEventListener('DOMContentLoaded', function() {
        var testimonialSwiper = new Swiper('.testimonial-slider', {
            slidesPerView: 1,
            spaceBetween: 30,
            loop: true,
            autoplay: {
                delay: 5000,
                disableOnInteraction: false,
            },
            pagination: {
                el: '.swiper-pagination',
                clickable: true,
            },
        });
        
        // Add active class to current page in navigation
        const currentPage = window.location.pathname.split('/').pop();
        const navLinks = document.querySelectorAll('.main-menu ul li a, .xb-menu-primary li a');
        
        navLinks.forEach(link => {
            const linkPage = link.getAttribute('href');
            if (linkPage === currentPage || (currentPage === '' && linkPage === 'index.html')) {
                link.parentElement.classList.add('active');
            }
        });
        
        // Animate metrics on scroll
        const observerOptions = {
            threshold: 0.5
        };
        
        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    const metricBars = entry.target.querySelectorAll('.metric-fill');
                    metricBars.forEach(bar => {
                        const width = bar.style.width;
                        bar.style.width = '0%';
                        setTimeout(() => {
                            bar.style.transition = 'width 1.5s ease-in-out';
                            bar.style.width = width;
                        }, 300);
                    });
                }
            });
        }, observerOptions);
        
        const metricsSection = document.querySelector('.performance-metrics');
        if (metricsSection) {
            observer.observe(metricsSection);
        }
    });
</script>

